import UIKit
extension UIButton {
    func setSelectedColor(){
        self.backgroundColor = #colorLiteral(red: 0.3024395704, green: 0.5301044583, blue: 0.2782174647, alpha: 1)
    }
    
    func setDeselectedColor(){
        self.backgroundColor = #colorLiteral(red: 0.4116270542, green: 0.7017195821, blue: 0.4307365417, alpha: 1)

    }
}
